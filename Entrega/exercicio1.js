/*Exercício 1 - Converta uma temperatura digitada em Celsius para Fahrenheit.

Dica: a formula do Fahrenheit = 9*Celsius/5 + 32 */

let temperaturaCelsius = prompt("Digite uma temperatura em Celsius: ")
let temperaturaF =   ((9 * temperaturaCelsius) / 5) + 32
console.log("A temperatura digitada em Celsius convertida para Fahrenheit é: " + temperaturaF)