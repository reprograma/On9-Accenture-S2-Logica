const diaDaSemana = 1;

switch (diaDaSemana) {
  case 1:
    console.log('domingo');
    break;
  case 2:
    console.log('segunda-feira');
    break;
  case 3:
    console.log('terça-feira');
    break;
  case 4:
    console.log('quarta-feira');
    break;
  case 5:
    console.log('quinta-feira');
    break;
  case 6:
    console.log('sexta-feira');
    break;
  case 7:
    console.log('sabado-feira');
    break;

  default:
    console.log('Acho que você escreveu um numero errado, tente novamente');
}
